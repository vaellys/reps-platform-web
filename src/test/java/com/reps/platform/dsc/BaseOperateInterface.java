package com.reps.platform.dsc;

import java.util.List;

public interface BaseOperateInterface {
	
	/**
	 * 
	* @author zds
	* @date  2016-4-20 上午10:47:11
	* @param relationList
	* @throws Exception
	* @return void
	 */
	public abstract void save(List<StandardDataRelation> relationList) throws Exception;
	
	/**
	 * 
	* @author zds
	* @date  2016-4-18 下午2:25:25
	* @param relationList 要修改的字段列表
	* @return
	* @throws Exception
	* @return boolean
	 */
	public abstract boolean update(List<StandardDataRelation> relationList) throws Exception ;
	
	/**
	 * 
	* @author zds
	* @date  2016-4-18 下午2:23:07
	* @param id 要删除的记录id
	* @return
	* @throws Exception
	* @return boolean
	 */
	public abstract boolean delete(String id) throws Exception;
	
	
}
